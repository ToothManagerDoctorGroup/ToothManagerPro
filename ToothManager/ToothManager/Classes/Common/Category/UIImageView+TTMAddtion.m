#import "UIImageView+TTMAddtion.h"

@implementation UIImageView (TTMAddtion)

- (void)circleImageViewWithCornRaduis:(CGFloat)raduis {
    self.layer.cornerRadius = raduis;
    self.layer.borderColor = TableViewCellSeparatorColor.CGColor;
    self.layer.borderWidth = TableViewCellSeparatorHeight;
    self.clipsToBounds = YES;
}

@end
